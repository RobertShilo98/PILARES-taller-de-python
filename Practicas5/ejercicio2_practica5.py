print("\nIngresa un número que este dentro del rango 1,7")
numero = float(input("Coloca aquí tu número  -  "))

if numero >= 1 and numero <= 7:
    print(f"\nTú número esta dentro del rango 1,7\nTú número {numero}")
else:
    print(f"\nError, tú número no esta dentro del rango 1,7\nTú número {numero}")

